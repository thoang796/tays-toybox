public class Rectangle {
  private int length;
  private int width;
  public void setLength(int length){
    this.length = length;
  }
  public void setWidth(int width){
    this.width = width;
  }
  public void getLength(){
    System.out.println(this.length);
  }
  public void getWidth(){
    System.out.println(this.width);
  }
}